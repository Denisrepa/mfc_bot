def smart_search(query):
    if not query: return None
    query_words = [w.lower() for w in query.split() if len(w) > 2]
    
    stop_words = ['–∫—É–ø–∏—Ç—å', '—Ö–æ—á—É', '—Ü–µ–Ω–∞', '—Å–∫–æ–ª—å–∫–æ', '—Å—Ç–æ–∏—Ç', '–∏—â—É', '–Ω—É–∂–Ω–∞', '–µ—Å—Ç—å']
    query_words = [w for w in query_words if w not in stop_words]

    if not query_words:
        return []

    scored_results = [] 

    for cat_key, cat_val in PRODUCTS.items():
        category_name = cat_val['name'].lower()
        
        def process_item(item_val, sub_key='none', item_key=''):
            full_search_text = (category_name + " " + item_val['name'] + " " + item_val['desc']).lower()
            
            score = 0
            for word in query_words:
                if word in full_search_text:
                    score += 1
            
            if score > 0:
                result_obj = {
                    'score': score,
                    'name': item_val['name'],
                    'price': item_val['price'],
                    'desc': item_val['desc'],
                    'cat': cat_key,
                    'sub': sub_key,
                    'item': item_key
                }
                scored_results.append(result_obj)

        if 'subcategories' in cat_val:
            for sub_key, sub_val in cat_val['subcategories'].items():
                for item_key, item_val in sub_val['items'].items():
                    process_item(item_val, sub_key, item_key)
        if 'items' in cat_val:
            for item_key, item_val in cat_val['items'].items():
                process_item(item_val, 'none', item_key)

    scored_results.sort(key=lambda x: x['score'], reverse=True)
    
    seen = set()
    unique_results = []
    for res in scored_results:
        if res['name'] not in seen:
            unique_results.append(res)
            seen.add(res['name'])
            
    return unique_results[:3]

